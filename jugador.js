class players{

    players(){
        this.name = '';
        this.score = 0;
        this.time  = ''; 
    }


    getName(){
        return this.name;
    }

    getScore(){
        return this.score
    }

    getTime(){
        return this.time
    }

    setName(name){
        this.name = name;
    }

    setScore(score){
        this.score = score;
    }

    setTime(time){
        this.time = time;
    }
}

